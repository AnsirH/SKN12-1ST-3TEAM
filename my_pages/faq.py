def faq_list(question, answer):
    """
    개별 FAQ 항목을 확장 가능한 형태로 표시하는 함수
    
    Args:
        question (str): FAQ 질문
        answer (str): FAQ 답변
        
    기능:
        - Streamlit의 expander를 사용하여 질문을 클릭하면 답변이 펼쳐지는 형태로 표시
        - 사용자가 원하는 질문만 선택적으로 답변을 볼 수 있도록 함
    """
    with st.expander(f"{question}"):
        st.write(f'{answer}')


